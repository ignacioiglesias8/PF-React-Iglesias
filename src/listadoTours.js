export const getTourById = (tourId) => {
    return new Promise((resolve) => {
        setTimeout(() => {
            resolve(tours.find(tour => tour.id === tourId))
        }, 1000)
    })
}

export const getTourBySubCategory = (subCategoryId) => {
    return new Promise((resolve) => {
        setTimeout(() => {
            resolve(tours.filter(tour => tour.subcategoria === subCategoryId))
        }, 1000)
    })
}